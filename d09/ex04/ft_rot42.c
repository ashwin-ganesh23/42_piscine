char	*ft_rot42(char *str)
{
	int c;
	int diff;

	diff = 0;
	c = 0;
	while (str[c] != '\0')
	{
		if (str[c] >= 65 && str[c] <= 90)
		{
			diff = (((str[c] - 65) + 42) % 26) + 65;
			str[c] = diff;
		}
		else if (str[c] >= 97 && str[c] <= 122)
		{
			diff = (((str[c] - 97) + 42) % 26) + 97;
			str[c] = diff;
		}
		c++;
	}
	return (str);
}
