int		ft_atoi(char *str)
{
	int		num;
	int		sign;
	char	p;

	num = 0;
	sign = 1;
	p = 0;
	if (str[p] == '-')
	{
		sign = -1;
		p++;
	}
	while ((str[p] >= '0') && (str[p] <= '9'))
	{
		num = num * 10 + (str[p] - '0');
		p++;
	}
	num = num * sign;
	return (num);
}
